    window.displayText = {
        'en': 'The site will be ready to work in a few hours',
        'pl': 'Strona będzie gotowa do pracy za kilka godziny',
        'de': 'Die Seite wird in wenigen Stunden einsatzbereit sein',
        'uk': 'Сайт буде готовий до роботи за кілька годин',
        'cn': '该网站将在几个小时内准备好使用',
        'ru': 'Сайт будет готов к работе через несколько часов'
    }

    window.addEventListener ?
        window.addEventListener("load", display, false) :
        window.attachEvent && window.attachEvent("onload", display);

    function display(){
        let default_lang = 'en',
            browser_lang = window.navigator.language || navigator.userLanguage,
            lang = '';

        browser_lang = browser_lang.substr(0, 2).toLowerCase();

        if(undefined !== window.displayText[browser_lang]){
            lang = browser_lang;
        }else{
            lang = default_lang;
        }

        document.querySelector('.error_details').innerHTML = window.displayText[lang];
        return false
    }

    setTimeout('location.reload()', 10000);
</script>
